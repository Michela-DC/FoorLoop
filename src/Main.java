public class Main {
    public static void main(String[] args) {
//        Use a for-loop to print all the number from 5 to 25 (including the upper and lower bound)
        int lowerBound = 5;
        int upperBound = 25;

        for(int i=lowerBound; i<=upperBound; i++) {
            System.out.println(i);
        }
    }
}

